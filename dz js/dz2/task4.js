/**
 * 4 Реализовать основные 4 арифметические операции (+, -, /, *) в виде функций с двумя
параметрами.
Обязательно использовать оператор return.
 */

 function sum(a, b){
     return a+b;
 }
 function subtraction(a, b){
     return a-b;
 }
 function divide(a, b){
     return a/b;
 }
 function multiplication(a, b){
     return a*b;
 }

 alert(sum(10,2));
 alert(subtraction(10,2));
 alert(divide(10,2));
 alert(multiplication(10,2));