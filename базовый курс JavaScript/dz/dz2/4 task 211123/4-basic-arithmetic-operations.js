'use strict';

/*
Реализовать основные 4 арифметические операции (+, -, /, *) в виде функций с двумя
параметрами.
Обязательно использовать оператор return.
*/

function sum(a, b) {
  return a + b
}

function subtraction(a, b) {
  return a - b
}

function devide(a, b) {
  return a / b
}

function multiplication(a, b) {
  return a * b
}

console.log(sum(3, 3));
console.log(subtraction(3, 3));
console.log(devide(3, 3));
console.log(multiplication(3, 3));
